@Component
@ConfigurationProperties(prefix = "test")
@PropertySource("classpath:test.properties")
public class TestSetting {
    private String a;
    private String b;

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }
}
